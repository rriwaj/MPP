package week1.day1.prob1.com.mum.projectmanagement;

public class Release {
	private int id;
	private String version;

	public void setId(int value) {
		this.id = value;
	}

	public int getId() {
		return this.id;
	}

	public void setVersion(String value) {
		this.version = value;
	}

	public String getVersion() {
		return this.version;
	}

}
